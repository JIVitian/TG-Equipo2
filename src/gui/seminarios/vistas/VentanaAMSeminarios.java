    public LocalDate jFecha(){
        LocalDate localDate;
        try {
            Date fecha = this.jDateChooser.getDate();
            Instant instante = Instant.ofEpochMilli(fecha.getTime());
            LocalDateTime localDateTime = LocalDateTime.ofInstant(instante, ZoneId.systemDefault());
            localDate = localDateTime.toLocalDate();
        }
        catch(NullPointerException e){
            return null;
        }
        return localDate;
    }
    
